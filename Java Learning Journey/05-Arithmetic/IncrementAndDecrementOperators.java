public class IncrementAndDecrementOperators {
    public static void main(String[] args) {

//        Increment and Decrement Operators

        int x  = 1;

        x++;
        x++;
        x++;

        System.out.println(x);

        int y = 2;

        y--;
        y--;
        y--;

        System.out.println(y);

    }
}
