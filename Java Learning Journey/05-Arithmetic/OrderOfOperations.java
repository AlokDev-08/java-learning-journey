public class OrderOfOperations {
    public static void main(String[] args) {

//        Order of Operations [ P-E-M-D-A-S]
//        P - Parenthesis
//        E - Exponents
//        M - Multiplication
//        D - Division
//        A - Addition
//        S - Substraction


        double result = 3+4*(7-5)/2.0;

        System.out.println(result);

    }
}
